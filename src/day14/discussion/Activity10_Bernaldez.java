package day14.discussion;

public class Activity10_Bernaldez {
    public static void main(String[] args) {
        
        // Math.abs() -> Returns the absolute (positive) value of a number.
        int num = -15;
        System.out.println(Math.abs(num));
        
        
        // Math.max() -> Returns the larger of two values.
        System.out.println(Math.max(25, 40));
        
        
        // Math.min() -> Returns the smaller of two values.
         System.out.println(Math.min(25, 40));
         
         
         // Math.pow() -> Returns the value of the first number raised to the power of the second number.
          System.out.println(Math.pow(2, 3));
          
          
          // Math.sqrt() -> Returns the square root of a number.
          System.out.println(Math.sqrt(64));
          
          
          // Math.round() -> Rounds a decimal number to the nearest whole number.
           System.out.println(Math.round(7.6));
           
           
           // Math.ceil() -> Rounds a decimal number up to the nearest whole number.
           System.out.println(Math.ceil(7.2));
           
           
           // Math.floor() -> Rounds a decimal number down to the nearest whole number.
           System.out.println(Math.floor(7.9));
           
           
           // Math.random() -> Returns a random decimal number between 0.0 (inclusive) and 1.0 (exclusive).
           System.out.println(Math.random());
           
           
           // Math.log() -> Returns the natural logarithm (base e) of a number.
           System.out.println(Math.log(10));
           
           
           // Math.log10() -> Returns the base-10 logarithm of a number.
           System.out.println(Math.log10(1000));
           
           
           // Math.exp() -> Returns the value of e raised to the specified power.
           System.out.println(Math.exp(2));
           
           
           // Math.sin() -> Returns the sine of an angle measured in radians.
            System.out.println(Math.sin(Math.PI / 2));
            
            
            // Math.cos() -> Returns the cosine of an angle measured in radians.
            System.out.println(Math.cos(0));
            
            
            // Math.tan() -> Returns the tangent of an angle measured in radians.
            System.out.println(Math.tan(Math.PI / 4));
    }
}
